#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*start;

	start = s1;
	s1 += ft_strlen(s1);
	if (s2)
		while (*s2)
			*s1++ = *s2++;
	*s1 = 0;
	return (start);
}
